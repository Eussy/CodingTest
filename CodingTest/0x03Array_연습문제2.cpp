// 문제 : 주어진 길이 N의 int 배열 arr에서 합이 100인 서로 다른 위치의 두 원소가 존재하면 1을, 존재하지 않으면 0을 반환하는 함수
//		  func2(int arr[], int N)을 작성하라.
//		  arr의 각 수는 0이상 100 이하이고 N은 1000 이하이다.

// func2({1, 52, 42}, 3) = 1,
// func2({50, 42}, 2) = 0,
// func2({4, 13, 63, 87}, 4) = 1




#include <bits/stdc++.h>
using namespace std;

// 핵심은 O(n2)가 아닌, O(N)의 시간 복잡도로 줄이는 것이 포인트이다.
int func2(int arr[], int N)
{
	int occur[101] = {0, };
	for (int i = 0; i < N; i++)
	{
		if (occur[100 - arr[i]] == 1)
			return 1;
		else
			occur[arr[i]] = 1;
	}
	return 0;
}

int main(void) {
	cin.tie(0);
	int arr[] = { 1, 52, 42 };
	cout << func2(arr, 3);
	int arr2[] = {50, 42};
	cout << func2(arr2, 2);
	int arr3[] = {4, 13, 63, 87};
	cout << func2(arr3, 4);
}	